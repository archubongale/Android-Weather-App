public class CurrentWeather {
    private String mIcon;
    private long mTime;
    private double mTemperature;
    private double mHumiduty;
    private double mPrecipChance;
    private String mSummry;

    public String getmIcon() {
        return mIcon;
    }

    public void setmIcon(String mIcon) {
        this.mIcon = mIcon;
    }

    public long getmTime() {
        return mTime;
    }

    public void setmTime(long mTime) {
        this.mTime = mTime;
    }

    public double getmTemperature() {
        return mTemperature;
    }

    public void setmTemperature(double mTemperature) {
        this.mTemperature = mTemperature;
    }

    public double getmHumiduty() {
        return mHumiduty;
    }

    public void setmHumiduty(double mHumiduty) {
        this.mHumiduty = mHumiduty;
    }

    public double getmPrecipChance() {
        return mPrecipChance;
    }

    public void setmPrecipChance(double mPrecipChance) {
        this.mPrecipChance = mPrecipChance;
    }

    public String getmSummry() {
        return mSummry;
    }

    public void setmSummry(String mSummry) {
        this.mSummry = mSummry;
    }



}
